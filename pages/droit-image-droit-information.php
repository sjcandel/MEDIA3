                            <li>
                                <h4>La portée informative</h4>
                                <p>La photographie se doit d’avoir une portée à but informatif et / ou à but historique. Nous nous expliquons à ce propos. Le but informatif est de rendre compte de l’actualité. Quant au but historique, il nous témoigne un évènement passé et illustre parfaitement son époque</p>
                            </li>
                            <li>
                                <h4>Le lien direct et le contexte</h4>
                                <p>Une personne présente et reconnaissable sur une image se doit d’être impérativement liée à l'événement présenté. De même, la photographie nécessite de ne pas être sortie de son contexte initial</p>
                                <img alt="Des fans d’indochine courant à toute vitesse au cabaret vert" src=""> 
                                <p>Prenons en exemple cette photographie. Dans son contexte de base, elle représente des fans du groupe Indochine sprintant pour obtenir le premier rang au Cabaret Vert. Il serait interdit d’utiliser cette image pour dire «Les marathoniens se déchaînent » ou encore « Le lâcher de taureau une tradition dangereuse ». En effet aucun lien ne peut être établi entre des fans d’Indochine et un marathon ou un lâcher de taureau… L’image est totalement sortie de son contexte d’origine.</p>
                            </li>
                            <li>
                                <h4>La dignité humaine</h4>
                                <p>En aucun cas une image ne peut porter atteinte à l’intimité ou à la dignité de la personne présente sur celle-ci. A savoir que la dignité humaine, est un principe mettant en lumière le fait qu’une personne ne doit ni être traitée comme un objet, ni comme un moyen mais bien comme une entité à part entière. Elle met en avant le respect d’autrui.</p> 
