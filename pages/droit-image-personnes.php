                    <article>
                        <h3>L'autorisation</h3>
                        <p>Il faut une autorisation pour prendre une personne reconnaissable en photo et pour la diffuser, sachez cependant qu’une photo est considérée comme autorisée si le sujet voit clairement le photographe et qu’il n’y a aucun mouvement visible de censure de cette image. Il faut cependant une autorisation écrite pour que la photographie puisse être reproduite et diffusée.
                        Cette autorisation doit contenir : </p>
                            <ul>
                                <li>Le support prévu</li>
                                <li>Le sujet</li>
                                <li>Le format</li>
                                <li>Le territoire géographique</li>
                                <li>La durée d’exploitation de l’image</li>
                                <li>Le nombre d’exemplaire</li>
                                <li>Eventuellement si la photo sera publiée en couleur ou en noir et blanc</li>
                            </ul>
                        
                            <p>Une nouvelle autorisation devra être demandée si l’objectif de la photo est différent de celui donné pour la première. (Par exemple, si une photo a été prise pour illustrer un article de journal, il faudra refaire une demande pour utiliser cette photo dans une publicité.)
                            <p>Certaines personnes ne peuvent cependant pas signer cette autorisation. Le droit à l’image protège les mineurs, les majeurs incapables, les personnes décédées et les professions sensibles. Dans les trois premiers cas, seuls des membres de la famille peuvent donner le droit à l’image de ce membre. Sachez cependant qu’un mineur doit avoir l’autorisation signée de ses deux parents, non d’un seul.</p>
                            <p>Pour les professions sensibles, donner des informations ou montrer des photos de certains militaires ou policiers est totalement interdit.
                            <p>Le droit à l’image interdit certains types de photographies même si elles peuvent entrer dans une exception, celle-ci ne peuvent en aucun cas être diffusées :</p>
                            <ul>
                                <li>Les images volées ou prises au téléobjectif</li>
                                <li>Même avec une autorisation, il est interdit de la diffuser avec un commentaire dévalorisant</li>
                            </ul>
                            <p>Et peuvent être punis de 1500€ d’amende :</p>
                            <ul>
                                <li>Les images d’une personne menottée sans son accord</li>
                                <li>Une image permettant d’identifier un mineur ayant quitté ses parents, délaissé, s’étant suicidé ou victime d’une infraction sans l’autorisation des parents</li>
                                <li>Diffusion sans son accord de renseignements d’une personne victime d’agression sexuelle</li>
                                <li>Donner des informations permettant d’identifier certains militaires ou policiers</li>
                            </ul>
                    </article>
